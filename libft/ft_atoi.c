#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	signe;
	int	r;

	i = 0;
	signe = 1;
	r = 0;
	while (s[i] == '\t' || s[i] == ' ' || s[i] == '\n'
		|| s[i] == '\r' || s[i] == '\v' || s[i] == '\f')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			signe = signe * -1 ;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9' && s[i])
	{
		r = r * 10 + s[i] - '0';
		i++;
	}
	return (r * signe);
}
